# กำหนดชื่อโมเดลที่ใช้
MODEL_NAME = "bert-base-uncased"  
# ใช้โมเดล BERT (Base) ที่ไม่คำนึงถึงตัวพิมพ์เล็กหรือพิมพ์ใหญ่ (uncased)

# กำหนดขนาดของเวกเตอร์ Embedding
EMBEDDING_DIM = 128  
# ขนาดของเวกเตอร์ที่ใช้ในการแปลงคำเป็นตัวเลข (Embedding) ในกรณีนี้กำหนดเป็น 128 มิติ

# กำหนดความยาวสูงสุดของประโยค (Token) ที่รองรับ
MAX_LENGTH = 512  
# จำนวน Token สูงสุดที่โมเดลสามารถประมวลผลในแต่ละอินพุตได้ (BERT มักรองรับสูงสุด 512)

# ตรวจสอบว่า GPU (CUDA) พร้อมใช้งานหรือไม่ และกำหนดอุปกรณ์ที่ใช้สำหรับการประมวลผล
DEVICE = "cuda" if torch.cuda.is_available() else "cpu"  
# ใช้ "cuda" หากมี GPU พร้อมใช้งาน มิฉะนั้นใช้ "cpu" สำหรับการประมวลผล
